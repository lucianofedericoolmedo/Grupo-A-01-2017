package edu.unq.desapp.groupA.backend.model;

public class Brand {

	// Instance Variables
	private String name;

	// Constructors
	public Brand() {
		
	}

	public Brand(String name) {
		this.name = name;
	}

	// Getters and Setters
	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

}
